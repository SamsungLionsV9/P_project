def get_msrp(model_name, is_imported=False):
    """모델명으로 신차가격 조회"""
    msrp_dict = IMPORTED_MSRP if is_imported else DOMESTIC_MSRP
    
    # 정확한 매칭
    if model_name in msrp_dict:
        return msrp_dict[model_name]
    
    # 부분 매칭 시도
    model_lower = model_name.lower()
    for key, value in msrp_dict.items():
        if key.lower() in model_lower or model_lower in key.lower():
            return value
    
    # 기본값 (국산 2500, 수입 6000)
    return 6000 if is_imported else 2500
